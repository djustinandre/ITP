// Primitive Data Types - values for singular values.

// Number: -12, 45, 1.5
let age = 25;
console.log("Age:", age);
console.log("Typeof age:", typeof age);

// Strings: "", ''
let firstName = "Jericho";
console.log("First Name:", firstName);
console.log("Typeof First Name:", typeof firstName);

// Boolean: true or false
let status = true;
status = false;
console.log("Status:", status);

// Null: Intentional
let car = null;
console.log("Car:", car);
console.log("Typeof car:",typeof car);

// Undefined: Unintentional
let city;
console.log("City:", city);
console.log("Typeof city:", typeof city);